package lcec.model;

import java.util.Date;

public class Biopsy implements java.io.Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;


	public Biopsy(){
		this.AutoNum = -1;
	}
	
	//`ProcedureType`, `ScheduleDate`, `ProcedureDate`, `PerformedBy`, `BiopsyLocationCode`, `LocationComment`
	private int procedureTypeId;
	private Date scheduleDate;
	private Date procedureDate;
	private String performedBy;
	private String biopsyLocation;
	private String locationComment;
	private String procedureName;
	
	
	public int getProcedureTypeId() {
		return procedureTypeId;
	}

	public void setProcedureTypeId(int procedureType) {
		this.procedureTypeId = procedureType;
	}

	public Date getScheduleDate() {
		return scheduleDate;
	}

	public void setScheduleDate(Date scheduleDate) {
		this.scheduleDate = scheduleDate;
	}

	public Date getProcedureDate() {
		return procedureDate;
	}

	public void setProcedureDate(Date procedureDate) {
		this.procedureDate = procedureDate;
	}
	
	public String getPerformedBy() {
		return performedBy;
	}

	public void setPerformedBy(String performedBy) {
		this.performedBy = performedBy;
	}

	public String getBiopsyLocation() {
		return biopsyLocation;
	}

	public void setBiopsyLocation(String biopsyLocation) {
		this.biopsyLocation = biopsyLocation;
	}

	public String getLocationComment() {
		return locationComment;
	}

	public void setLocationComment(String locationComment) {
		this.locationComment = locationComment;
	}

	//`Complication`,`Biopsy Result`, `Result-Diagnostic`, `BiopsyAccuracy`
	private String complication;
	private String biopsyResult;
	private String diagnostic;
	private String accuracy;
	
	
	public String getComplication() {
		return complication;
	}

	public void setComplication(String complication) {
		this.complication = complication;
	}

	public String getBiopsyResult() {
		return biopsyResult;
	}

	public void setBiopsyResult(String biopsyResult) {
		this.biopsyResult = biopsyResult;
	}

	public String getDiagnostic() {
		return diagnostic;
	}

	public void setDiagnostic(String diagnostic) {
		this.diagnostic = diagnostic;
	}

	public String getAccuracy() {
		return accuracy;
	}

	public void setAccuracy(String accuracy) {
		this.accuracy = accuracy;
	}

	//`Comment`
	private String comment;
	private int AutoNum;

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public int getAutoNum() {
		return AutoNum;
	}

	public void setAutoNum(int autoNum) {
		AutoNum = autoNum;
	}

	public String getProcedureName() {
		return procedureName;
	}

	public void setProcedureName(String procedureName) {
		this.procedureName = procedureName;
	}
	
}
